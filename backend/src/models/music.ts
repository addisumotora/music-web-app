import mongoose, { Schema } from "mongoose";
import { Music } from "../types/music";

const musicSchema: Schema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    genre: {
      type: String,
      required: true,
    },
    artist: {
      type: String,
      required: true,
    },
    album: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Music = mongoose.model<Music>("Music", musicSchema);

export default Music;
